package day02;


/*
   참고] 
   		이클립스에서 블럭을 만들경우 {} 사용하게 되는데 
   		이떄 이클립스는 자동으로 닫는 괄호( } ) 붙여준다
   		
 */
public class Test08 {
	public static void main(String[] args) {
		int no1 = 10;
		int no2 = 11;
		
		++no1;
		
		
		System.out.println("no1 : " + no1);
		System.out.println("no2 : " + no2);
		
	}

}
